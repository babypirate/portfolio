				<h5 class="section__title">The 3 R's Of Habit Formation - Reminder, Routine, Reward</h5>
				<p class="content__copy">James Clear, author of the New York Times bestseller Atomic Habits, introduced a simple 3-step pattern of habit formation.</p>
				<p class="content__copy">1. Reminder (the trigger that initiates the behavior)</p>
				<p class="content__copy">2. Routine (the behavior itself; the action you take)</p>
				<p class="content__copy">3. Reward (the benefit you gain from doing the behavior)</p>
