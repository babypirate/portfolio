				<img class="flex-child-small-img" src="img/teamup/habitshare.png" alt="HabitShare logo ">
				<p class="content__copy tc">HabitShare<p>
			</div>
		</div>
		<div class="section__flexcontent flex-column">
			<p>I researched the market to see how they are trying to solve this problem and thought about what I could do differently. I found that:</p>
			<ul>
				<li>Habit tracking apps such as Streaks and Habit are based on individual performances and don’t give users outer accountability, which might not work for people who can’t keep commitments to themselves.</li>
				<li>Current habit tracking apps usually give users visual representations of how many consecutive days users have completed their habits. The don’t-break-the-chain mentality makes users motivate by fear of breaking the streak. However, once the users break the chain, they may feel guilty or even lose confidence.</li>
				<li>Streaks and Habit let uses set daily reminders for habits, but some users might find daily push alert reminders annoying, and may even resent the app for telling them what to do.</li>
				<li>HabitShare is the only habit tracker that is social. But its social network is restricted to friends only.</li>
			</ul>
