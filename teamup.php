				<h5 class="section__title">Experts' suggestions for adopting better habits</h5>
				<p class="content__copy">Professor BJ Fogg, a Behavior Scientist at Stanford and expert in habit formation, recommends starting small and taking on one new habit at a time. By taking tiny steps to start, Fogg found that people are more likely to keep that habit because the new behaviors don’t require a lot of motivation at first.</p>
